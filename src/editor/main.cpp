#include "editor.hpp"

#if defined(ERHE_WINDOW_LIBRARY_SDL)
#   include <SDL3/SDL_main.h>
#endif

#if defined(ERHE_OS_ANDROID)
#   include "erhe_file/file.hpp"
#   include <SDL3/SDL_system.h>
#   include <unistd.h>
#endif

auto main(int, char**) -> int
{
#if defined(ERHE_OS_ANDROID)
    // Android starts the process at cwd "/", which is read-only and outside
    // the app sandbox. Move to internal storage so relative writes
    // (spirv_cache/, generated configs) land under /data/data/<pkg>/files/.
    if (const char* base = SDL_GetAndroidInternalStoragePath()) {
        ::chdir(base);
    }

    // First-launch migration: copy every config/* file bundled in the APK
    // to the writable cwd, unless the destination already exists. After
    // this, erhe::file::read tries the writable copy first (then falls
    // back to APK assets), so save_config writes from the Settings window
    // round-trip correctly. The manifest is generated by stageErheAssets
    // in android-project/app/build.gradle.
    (void)erhe::file::migrate_android_assets_to_writable("erhe_config_manifest.txt");
#endif
    editor::run_editor();
    return 0;
}
